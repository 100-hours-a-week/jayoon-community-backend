#!/bin/bash
# (이 파일은 EC2에서 'sudo'로 실행될 것입니다)

# --- 스크립트 오류 시 즉시 중단 ---
set -e

echo "========================================"
echo ">> EC2(Ubuntu) 초기 설정을 시작합니다."
echo "========================================"

# --- 1. 패키지 매니저 업데이트 ---
echo ">> (1/4) apt-get 업데이트..."
sudo apt-get update -y

# --- 3. Nginx 설치 (FE 서빙 및 리버스 프록시용) ---
echo ">> (3/4) Nginx 설치..."
sudo apt-get install -y nginx

echo "========================================"
echo ">> ✅ 모든 설치가 완료되었습니다."
echo "========================================"

# --- 버전 확인 ---
echo "--- 설치된 버전 ---"
nginx -v
